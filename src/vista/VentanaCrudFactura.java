    private void tablaDetalleKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tablaDetalleKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_TAB) {

            String codigo = tablaDetalle.getValueAt(tablaDetalle.getSelectedRow(), 0) + "";
            Producto c = controladorProducto.buscar(codigo);
            if (c != null) {
                if (c.getStock() > 0) {
                    int numero = 0;
                    boolean bandera = false;
                    tablaDetalle.setValueAt(c.getNombre(), tablaDetalle.getSelectedRow(), 1);

                    do {
                        try {
                            numero = Integer.parseInt(javax.swing.JOptionPane.showInputDialog("Ingrese Cantidad en " + c.getMedida()
                                    + "\n Existen: " + c.getStock() + " " + c.getMedida()));
                            if (numero <= c.getStock()) {
                                bandera = true;
                            } else {
                                javax.swing.JOptionPane.showMessageDialog(this, "INGRES UNA CANTIDAD MENOR A: " + c.getStock());
                            }
                        } catch (NumberFormatException e) {
                            javax.swing.JOptionPane.showMessageDialog(this, "Solo se permiten numeros");
                            bandera = false;
                        }
                    } while (!bandera);
                    int cant = numero;
                    tablaDetalle.setValueAt(cant, tablaDetalle.getSelectedRow(), 2);

                    tablaDetalle.setValueAt(c.getPrecio(), tablaDetalle.getSelectedRow(), 3);
                    tablaDetalle.setValueAt(cant * c.getPrecio(), tablaDetalle.getSelectedRow(), 4);
                    double subtotal = 0;
                    for (int i = 0; i < tablaDetalle.getRowCount(); i++) {

                        if (tablaDetalle.getValueAt(i, 4).toString().length() > 0) {

                            subtotal = subtotal + (double) tablaDetalle.getValueAt(i, 4);
                            txtSubtotal.setText(subtotal + "");
                        }
                    }

                    modelotabla = (DefaultTableModel) tablaDetalle.getModel();
                    modelotabla.addRow(new Object[]{"", "", "", "", ""});
                    tablaDetalle.setModel(modelotabla);
                    double iva = subtotal * 0.12;
                    if (comboIva.getSelectedIndex() == 1) {
                        iva = 0;
                    }
                    txtIVA.setText(iva + "");
                    double total = iva + subtotal;
                    txtTotalFactura.setText(total + "");

                } else {
                    JOptionPane.showMessageDialog(this, "Ya no existen productos: Por favor ingrese mas " + c.getMedida());
                }

            } else {
                JOptionPane.showMessageDialog(this, "No se a encontrado el producto");
            }
        }
    }//GEN-LAST:event_tablaDetalleKeyReleased

