    private void btnGuardarFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarFacturaActionPerformed
        int cont = 0;
        String mensaje = "";
        if (!txtCedula.getText().equals("")) {
            cont++;
        } else {
            mensaje = "Falta cliente";
        };
        if (tablaDetalle.getRowCount() - 1 >= 1) {
            cont++;
        } else {
            mensaje = "Falta productos";
        };

        if (cont == 2) {
            Factura f = new Factura();
            f.setCodigo(controladorFactura.obtenerCodigo());
            f.setCliente(controladorCliente.buscar(txtCedula.getText()));
            f.setSubtotal(Double.parseDouble(txtSubtotal.getText()));
            f.setIva(Double.parseDouble(txtIVA.getText()));
            f.setTotal(Double.parseDouble(txtTotalFactura.getText()));
            f.setNotas(txtNotas.getText() + " ");
            f.setFecha(txtFecha.getText());
            boolean op = controladorFactura.crear(f);
            if (op) {
                for (int i = 0; i < tablaDetalle.getRowCount(); i++) {
                    if (tablaDetalle.getValueAt(i, 4).toString().length() > 0) {
                        Detalle d = new Detalle();
                        d.setCodigo(controladorDetalle.obtenerCodigo());
                        String codigo = String.valueOf(tablaDetalle.getValueAt(i, 0));
                        d.setProducto(controladorProducto.buscar(codigo).getId());

                        int cant = Integer.parseInt(tablaDetalle.getValueAt(i, 2).toString());
                        d.setCantidad(cant);
                        d.setFactura(f.getCodigo());
                        controladorDetalle.crear(d);
                        controladorProducto.disminuir(codigo, cant);
                    }
                }
                int impr = JOptionPane.showConfirmDialog(this, "Desea imprmir");
                if (impr == JOptionPane.OK_OPTION) {
                    controladorFactura.impirmir(f.getCodigo());
                } else {
                    JOptionPane.showMessageDialog(this, "Podras Imprimir Cuando Listes");
                }

                Date date = new Date();
                DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                txtFecha.setText(dateFormat.format(date));
                txtCedula.setText("");
                txtSubtotal.setText("");
                txtIVA.setText("");
                txtTotalFactura.setText("");
                txtNotas.setText("");
                txtCedula.setText("");
                txtCliente.setText("");
                txtTelefono.setText("");
                txtDireccion.setText("");
                txtCorreo.setText("");

                modelotabla = null;
                modelotabla = (DefaultTableModel) tablaDetalle.getModel();
                while (modelotabla.getRowCount() > 0) {
                    for (int j = 0; j < modelotabla.getRowCount(); j++) {
                        modelotabla.removeRow(j);
                    }
                }
                modelotabla.addRow(new Object[]{"", "", "", "", ""});
                tablaDetalle.setModel(modelotabla);

            } else {
                JOptionPane.showMessageDialog(this, "Erro con la base");
            }

        } else {
            JOptionPane.showMessageDialog(this, mensaje);

        }
    }//GEN-LAST:event_btnGuardarFacturaActionPerformed

